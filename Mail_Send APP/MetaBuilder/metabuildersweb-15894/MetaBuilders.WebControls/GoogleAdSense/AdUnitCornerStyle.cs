using System;

namespace MetaBuilders.WebControls
{
	/// <summary>
	/// The possible values for the style of border corners on an AdSense ad.
	/// </summary>
	public enum AdUnitCornerStyle
	{
		/// <summary>
		/// Corners will be square. The Default.
		/// </summary>
		Square = 0,

		/// <summary>
		/// Corners will be slightly rounded.
		/// </summary>
		SlightlyRounded = 6,

		/// <summary>
		/// Corners will be very rounded.
		/// </summary>
		VeryRounded = 10
	}
}
