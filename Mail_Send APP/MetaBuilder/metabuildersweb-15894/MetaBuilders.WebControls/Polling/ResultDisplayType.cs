using System;

namespace MetaBuilders.WebControls
{
	/// <summary>
	/// Specifies how vote counts are displayed in the result view of a <see cref="PollView"/> control.
	/// </summary>
	public enum ResultDisplayType
	{
		/// <summary>
		/// The votes on an answer will be displayed as a percentage of total votes.
		/// </summary>
		Percentage,
		/// <summary>
		/// The votes on an answer will be displayed as the exact vote count.
		/// </summary>
		Count
	}
}
